public class Concept {

    /*
     ¿Qué son los constructores en Java?

     Los constructores en Java son un método que se utiliza para establecer valores iniciales para variables de campo.
     En Java, cuando se crea el objeto, el compilador llama primero al constructor.
     Significa que cualquier código escrito en el constructor se ejecutará.

     Además, no es necesario realizar ninguna llamada especial a un método constructor:
     ocurre automáticamente en el tiempo de ejecución cuando el compilador crea un nuevo objeto.

     Los métodos constructores toman el mismo nombre que la clase.
     */
}
