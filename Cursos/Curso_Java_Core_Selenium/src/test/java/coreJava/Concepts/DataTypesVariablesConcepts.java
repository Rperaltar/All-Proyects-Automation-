    /*
    Tipos de datos y variables en "Java"
    Tener una buena comprensión de los tipos de datos y las variables es el paso básico para comprender la programación.

    ¿Qué es variable?
    Como sugieren sus nombres, variable es un valor que puede cambiar. Un programa de computadora simple utiliza un conjunto de instrucciones y datos.
    Los datos pueden ser constantes o valores fijos que nunca cambian y pueden ser variables que pueden cambiar durante la ejecución.
    En lugar de ingresar datos directamente en un programa, un programador puede usar variables para representar los datos.
    Cuando el compilador ejecuta el programa,
    las variables utilizadas en las instrucciones se reemplazan con los datos reales ingresados por el programador.
    */

    /*
    ¿Cómo funcionan las variables?
    Declaración de variables: solicite al compilador que asigne cierta cantidad de memoria a la variable para almacenar algún valor.
    Como compilador, le gustaría saber el tipo de datos de la variable para poder asignar solo la memoria requerida a la variable.
    Eso se puede hacer 'declarando el tipo de datos' de la variable.
    Nomenclatura de variables: después de la declaración de la variable, cuando el programa necesita el valor almacenado,
    el compilador irá a la ubicación de la memoria para buscar el valor y devolverlo al programa.
    Para manejar efectivamente esta transacción,
    el compilador necesitaría dos piezas de información del programa: nombre y tipo de datos de la variable.
    Asigne el nombre de su elección a la variable e infórmele al compilador.
    De modo que la próxima vez que haga referencia a ese nombre, el compilador sabrá a qué parte de la memoria se refiere.
    El nombre de la variable se denomina Identificador.
    Este proceso se llama Declaración de Variables.
    TipoDeDatosDeVariable VariableName;
    Inicialización de variables: una vez que se realiza la declaración de la variable, se puede almacenar un valor en la ubicación de memoria reservada.
    Antes de eso, no podría usar la variable. La inicialización de una variable es muy importante antes de usar la variable para cualquier tipo de operación.
    Poner un valor inicial se conoce como inicializar la variable.

    //ejemplo
    NombreVariable = Valor;

    /*
    ¿Qué es el tipo de datos?

    Un tipo de datos indica qué tipo de valor o el tipo de datos que puede representar la variable, como números enteros,
    números de coma flotante, caracteres, valores booleanos o cadenas alfanuméricas.
    También hay otros tipos de datos, como cortos, largos y flotantes,
    pero en la programación de Selenium es posible que no se enfrente a ninguna situación en la que tenga que usar estos tipos de datos.
    Estos tipos de datos se utilizan cuando cada byte de memoria es importante para un mejor rendimiento del sistema.
    Ejemplos de diferentes tipos de datos
    Tipo de datos: booleano
    El tipo de datos booleano se usa para almacenar solo valores 'booleanos' como 'verdadero' y 'falso'.
    Para declarar una variable booleana, puede usar la palabra clave 'boolean'.

 */

