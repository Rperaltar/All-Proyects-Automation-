package CoreJava.Loops;

public class Concept {

    /*
     Bucles en Java:

     Como se explicó anteriormente, los bucles se utilizan para ejecutar un conjunto de declaraciones repetidamente hasta que se cumple una condición particular.

     En Java tenemos tres tipos de bucles básicos: for, while y do-while.

     ¿Cuáles son los diversos tipos de bucles en Java?
     El bucle for: Esto ejecuta una declaración para un número particular de veces
     El bucle while: ejecuta una declaración un número desconocido de veces.
     El ciclo do-while: Esto ejecuta una declaración al menos por una vez
     Bucle for mejorado: este bucle for también funciona con una matriz de cualquier tipo

     El bucle for en Java

     Un ciclo for es una estructura de control de repetición que le permite escribir de manera eficiente un ciclo que necesita ejecutarse un número específico de veces. La sintaxis para el bucle "for" es:

        // Example for
     for(Inicialización de variable; Prueba de expresión booleana; Incremento/Decremento){
        //Declaraciones
}
     */

    /*
    Tratemos de entender el código anterior. Se divide en cuatro partes:

    Declaración de variable: esta parte es la primera parte y se denomina Inicialización de variable.
    Esto especifica en qué número desea iniciar el bucle y esto se ejecuta solo una vez en el proceso de bucle completo.
    Condición: la segunda parte utiliza alguna lógica condicional en la que se evalúa la expresión regular.
    El cuerpo del ciclo se ejecuta solo si la condición es verdadera. En caso de falso, el flujo del bucle salta fuera del bucle.
    Cuerpo: como se dijo anteriormente, esto se ejecuta solo si la condición de la expresión es verdadera.
    El cuerpo puede ser la serie de pasos y también puede tener otro bucle for.
    El cuerpo muy a menudo hace uso de la variable declarada en la primera parte del bucle.
    Declaración de incremento: una vez que se ejecuta el cuerpo, el flujo va a esta declaración y cambia el valor de la variable declarada
    en la primera parte del ciclo. En realidad, incrementa el valor en caso de ++ y decrementa el valor en caso de --.
     */
}
